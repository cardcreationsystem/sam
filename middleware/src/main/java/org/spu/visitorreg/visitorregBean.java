package org.spu.visitorreg;

import java.sql.Date;

public class visitorregBean {
private int visitorreg_Id;
private String visitorreg_Name;
private int visitorreg_Contact;
private Date visitorreg_Date;
private String visitorreg_Email;
public int getVisitorreg_Id() {
	return visitorreg_Id;
}
public void setVisitorreg_Id(int visitorreg_Id) {
	this.visitorreg_Id = visitorreg_Id;
}
public String getVisitorreg_Name() {
	return visitorreg_Name;
}
public void setVisitorreg_Name(String visitorreg_Name) {
	this.visitorreg_Name = visitorreg_Name;
}
public int getVisitorreg_Contact() {
	return visitorreg_Contact;
}
public void setVisitorreg_Contact(int visitorreg_Contact) {
	this.visitorreg_Contact = visitorreg_Contact;
}
public Date getVisitorreg_Date() {
	return visitorreg_Date;
}
public void setVisitorreg_Date(Date visitorreg_Date) {
	this.visitorreg_Date = visitorreg_Date;
}
public String getVisitorreg_Email() {
	return visitorreg_Email;
}
public void setVisitorreg_Email(String visitorreg_Email) {
	this.visitorreg_Email = visitorreg_Email;
}



}
